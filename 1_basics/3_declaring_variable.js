/**
 * 변수 선언
 * 
 * 1) var - 이제 안 씀
 * 2) let
 * 3) const
 */

var name = 'dusdn';
console.log(name);

var age = 32;
console.log(age);

let ive = '아이브';
console.log(ive);

/**
 * let과 var로 선언한 변수는
 * 값 변경 가능
 */

ive = '안유진';
console.log(ive);

const newJeans = '뉴진스';
console.log(newJeans);

